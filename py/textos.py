# Trabajar lineas de texto en python

# Comillas simples y dobles
print("Hola, esta es una 'palabra' entre comillas simples")

# Saltos de linea
print("Palabras con\nSaltos de linea")

# Tabulaciones
print("Palabras con\ttabulaciones")

# Impresion en modo raw, que evita la interpretacion de comandos especiales como '\n'
print(r"Home\sers\aaronvegu\code\new")

# Asignacion de variables
c = "Esta en una linea\ncon saltos"
print(c)

# Multiplicacion de cadenas
diezEspacios = " " * 10
print(diezEspacios + "una cadena con diez espacios")